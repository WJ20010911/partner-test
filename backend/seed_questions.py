def rebuild():
    questions = json.loads(QUESTIONS_JSON)
    db = get_db()
    cur = db.cursor()

    # Clear old related data
    cur.execute("DELETE FROM question_tags")
    cur.execute("DELETE FROM tags")
    cur.execute("DELETE FROM test_records")
    cur.execute("DELETE FROM question_skips")
    cur.execute("DELETE FROM questions")
    db.commit()
    print("Cleared old questions and related data.")

    now = datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')
    inserted = 0

    for q in questions:
        qid = str(uuid.uuid4())

        # Serialize options as JSON string
        options_json = json.dumps(q["options"], ensure_ascii=False)
        time_limit = q.get("time_limit", 0)
        weight = q.get("weight", 1.0)

        # Check if time_limit column exists in this DB
        cols = [c[1] for c in cur.execute("PRAGMA table_info(questions)").fetchall()]
        if "time_limit" in cols:
            cur.execute(
                """INSERT INTO questions
                   (id, content, options, dimension, weight, time_limit, status, created_at)
                   VALUES (?,?,?,?,?,?,'approved',?)""",
                (qid, q["content"], options_json, q["dimension"], weight, time_limit, now)
            )
        else:
            cur.execute(
                """INSERT INTO questions
                   (id, content, options, dimension, weight, status, created_at)
                   VALUES (?,?,?,?,?,'approved',?)""",
                (qid, q["content"], options_json, q["dimension"], weight, now)
            )

        # Insert tags
        for tag_name in q.get("tags", []):
            tag_name = tag_name.strip()
            if not tag_name:
                continue
            existing = cur.execute("SELECT id FROM tags WHERE name = ?", (tag_name,)).fetchone()
            if existing:
                tid = existing["id"]
            else:
                tid = str(uuid.uuid4())
                cur.execute("INSERT INTO tags (id, name) VALUES (?, ?)", (tid, tag_name))
            cur.execute("INSERT OR IGNORE INTO question_tags (question_id, tag_id) VALUES (?, ?)", (qid, tid))

        inserted += 1

    db.commit()
    db.close()
    print(f"Inserted {inserted} questions successfully.")

if __name__ == "__main__":
    rebuild()